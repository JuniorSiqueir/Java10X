package ParaEstudar;

public interface Interagivel {
    /**
     * INTERFACE:
     * Uma interface define um "contrato" que as classes podem implementar.
     * Ela contém apenas assinaturas de métodos (e constantes). Uma classe que
     * implementa uma interface é obrigada a fornecer uma implementação para todos
     * os seus métodos.
     */
        void interagir();
    }

